    <h2>¿Qué es una cookie?</h2>
    <p>Una cookie es un fichero que se descarga en su ordenador al acceder a determinadas páginas web. Las cookies permiten a una página web,
        entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación de un usuario o de su equipo y, dependiendo de
        la información que contengan y de la forma en que utilice su equipo, pueden utilizarse para reconocer al usuario. </p>

    <h2>¿Qué tipos de cookies utiliza www.cybergangas.com?</h2>
    <p>Según el plazo de tiempo que permanecen activas las cookies pueden ser: </p>
    <ul class="ulWithSquare">
        <li class="liDos"><b>Cookies de sesión</b>: diseñadas para recabar y almacenar datos mientras el usuario accede a una página web.
            Se suelen emplear para almacenar información que sólo interesa conservar para la prestación del servicio solicitado por
            el usuario en una sola ocasión (por ejemplo, una lista de productos adquiridos). </li>
        <li class="liDos"><b>Cookies persistentes</b>: Son un tipo de cookies por las que los datos siguen almacenados en el terminal y
            puede accederse a ellos y ser tratados durante un periodo definido. Tienen fecha de borrado. Se utilizan por ejemplo en
            el proceso de compra o registro para evitar tener que introducir nuestros datos constantemente. </li>
        <br>
        <li><b>Nuestras cookies tienen una duración de <u>1 hora</u> desde que hace login. <u style="color: red">Si cierra el navegador seguirá estando logeado hasta 1 hora después del inicio de sesión.</u></b></li>
    </ul>

